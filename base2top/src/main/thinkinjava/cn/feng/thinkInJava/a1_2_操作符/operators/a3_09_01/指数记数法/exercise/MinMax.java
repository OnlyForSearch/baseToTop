package cn.feng.thinkInJava.a1_2_操作符.operators.a3_09_01.指数记数法.exercise;

/**
 * 分别显示用float和double指数计数法所能表示最大和最小数字
 * Created by fengyu on 2015-09-29.
 */
public class MinMax {
    
    public static void main(String args[]) {
        final double maxValue = Double.MAX_VALUE;
        System.out.println("Double maxValue = " + maxValue);
        final double minValue = Double.MIN_VALUE;
        System.out.println("Double minValue = " + minValue);
        final float maxValue1 = Float.MAX_VALUE;
        System.out.println("Float maxValue1 = " + maxValue1);
        final float minValue1 = Float.MIN_VALUE;
        System.out.println("Float minValue1 = " + minValue1);
    }
}
